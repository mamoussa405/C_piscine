int		ft_is_prime(int nb)
{
	int result;
	int i;
	int j;

	result = 0;
	i = 1;
	j = 0;
	while (i <= nb)
	{
		if ((nb % i) == 0)
		{
			result = 1;
			j++;
		}
		if (j != 2)
		{
			result = 0;
		}
		i++;
	}
	return (result);
}
