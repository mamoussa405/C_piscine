int		ft_sqrt(int nb)
{
	int result;
	int i;
	int j;

	result = 0;
	i = 1;
	j = 0;
	while ((i <= nb) && (j != 1))
	{
		if ((i * i) == nb)
		{
			result = i;
			j = 1;
		}
		i++;
	}
	return (result);
}
